package com.shsnc.entity.system;



/**
 * 历史信息
 */
public class Bean {
    private String app;
    private String module;
    private Short threadthreshold;
    private String ip;
    private String port;
    private String server;
    private Short threadtotal;
    private Short threadidle;
    private Short threadrun;
    private String value;
    private String system;
    private String name;


    public String getApp() {
        return app;
    }

    public void setApp(String app) {
        this.app = app;
    }

    public String getModule() {
        return module;
    }

    public void setModule(String module) {
        this.module = module;
    }

    public Short getThreadthreshold() {
        return threadthreshold;
    }

    public void setThreadthreshold(Short threadthreshold) {
        this.threadthreshold = threadthreshold;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getServer() {
        return server;
    }

    public void setServer(String server) {
        this.server = server;
    }

    public Short getThreadtotal() {
        return threadtotal;
    }

    public void setThreadtotal(Short threadtotal) {
        this.threadtotal = threadtotal;
    }

    public Short getThreadidle() {
        return threadidle;
    }

    public void setThreadidle(Short threadidle) {
        this.threadidle = threadidle;
    }

    public Short getThreadrun() {
        return threadrun;
    }

    public void setThreadrun(Short threadrun) {
        this.threadrun = threadrun;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getSystem() {
        return system;
    }

    public void setSystem(String system) {
        this.system = system;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
